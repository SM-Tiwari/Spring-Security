// Action Button for Mobile
$(document).ready(function () {
    $('.act-btn-toggle').on("click", function () {
        $(this).toggleClass('open');
        $('.option').toggleClass('scale-on');
    });
});

// Focused Dropdown Menu
$(document).ready(function() {
    $('a.getFocus, .dropdown-menu').hover(function() {
        $('.overlay').addClass('active');
    }, function() {
        $('.overlay').removeClass('active');
    });
});

// Promotion Popover
$(document).ready(function() {
    $('#promoPopover').popover({
        html : true,
        trigger: "click",
        content: function() {
            return $('#promoPopoverContent').html();
        }
    });
});

